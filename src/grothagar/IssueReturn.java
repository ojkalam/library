    private void issuebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_issuebtnActionPerformed
        // TODO add your handling code here:
        //issue book code is here
                
        String sql="insert into issuebook(Book_ID,Book_Name,Author,Publisher,Edition,Pages,Student_ID,St_Name,Semester,Email,Contact,Address,Issue_Date) values (?,?,?,?,?,?,?,?,?,?,?,?,?)";
        try{
            con = Db.getConnection();
             
            pst=con.prepareStatement(sql);
            
            pst.setString(1, searchkey.getText());
            pst.setString(2, name.getText());
            pst.setString(3, author.getText());
            pst.setString(4, publisher.getText());
            pst.setString(5, edition.getText());
            pst.setString(6, page.getText());
            pst.setString(7, st_searchkey.getText());
            pst.setString(8, st_name.getText());
            pst.setString(9, semester.getText());
            pst.setString(10, email.getText());
            pst.setString(11, contact.getText());
            pst.setString(12, address.getText());
            pst.setString(13, ((JTextField)issuedate.getDateEditor().getUiComponent()).getText());
            pst.executeUpdate();
            
            pst.close();
            con.close();
            
            JOptionPane.showMessageDialog(null, "Book Issued successfully !");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try{      
                pst.close();
                con.close();
            }catch(Exception ee){
                  JOptionPane.showMessageDialog(null,"Connection error","Warning",JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_issuebtnActionPerformed

