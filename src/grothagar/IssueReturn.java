    private void srcbtn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_srcbtn2ActionPerformed
        // TODO add your handling code here:
        //member info search
          String sql = "select Student_ID,Name,Semester,Email,Contact,Address from member where Student_ID = ?";
       
        try{
            con = Db.getConnection();
            
            pst=con.prepareStatement(sql);
            pst.setString(1, st_searchkey.getText());
  
            rs = pst.executeQuery();
            if(rs.next()){
                String add1=rs.getString("Name");
                st_name.setText(add1);
                String add2=rs.getString("Semester");
                semester.setText(add2);
                String add3=rs.getString("Email");
                email.setText(add3);
                String add4=rs.getString("Contact");
                contact.setText(add4);
                String add5=rs.getString("Address");
                address.setText(add5);
                rs.close();
                pst.close();
                con.close();
            }
            else{
                JOptionPane.showMessageDialog(null,"Student ID not found !","Not found",JOptionPane.WARNING_MESSAGE);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try{
                rs.close();
                pst.close();
                con.close();
            }catch(Exception ee){
                  JOptionPane.showMessageDialog(null,"Connection error","Warning",JOptionPane.WARNING_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_srcbtn2ActionPerformed

