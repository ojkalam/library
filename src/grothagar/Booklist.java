     public void fetch_books(){
   
       Connection con = null;
        Statement stmt = null;
        ResultSet rs = null;
       try{
           String query = "select * from books";
           con = Db.getConnection();
           stmt = con.createStatement();
           rs = stmt.executeQuery(query);
           booktable.setModel(DbUtils.resultSetToTableModel(rs));

           booktable.setAutoResizeMode(booktable.AUTO_RESIZE_LAST_COLUMN);
           booktable.setRowHeight(30);
           booktable.getColumnModel().getColumn(0).setMaxWidth(50);
           booktable.getColumnModel().getColumn(2).setMinWidth(160);
           booktable.getColumnModel().getColumn(3).setMinWidth(100);
           booktable.getColumnModel().getColumn(4).setMinWidth(120);
           booktable.getColumnModel().getColumn(5).setMinWidth(130);
        }catch(Exception e){
                  JOptionPane.showMessageDialog(null, e);
        }finally {
            try{
                rs.close();
                stmt.close();
                con.close();
            }catch(Exception ee){
                JOptionPane.showMessageDialog(null,"Connection error","Warning",JOptionPane.WARNING_MESSAGE);
            }
        } 
    }

