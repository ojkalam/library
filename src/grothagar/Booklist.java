    private void delbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delbtnActionPerformed
        // TODO add your handling code here:
        //Delete selected table
        Connection conn = null;
        PreparedStatement ps = null;
        DefaultTableModel model= (DefaultTableModel)booktable.getModel();
        int row = booktable.getSelectedRow();
        String selected = model.getValueAt(row, 0).toString();

        if (row >= 0) {
            model.removeRow(row);
            try {
                conn = Db.getConnection();
                ps = conn.prepareStatement("delete from books where Serial='"+selected+"' ");
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null," Book Deleted Successfully !","Success",JOptionPane.INFORMATION_MESSAGE);

            }catch(Exception ee){
                JOptionPane.showMessageDialog(null,"Error during Delete !","warning",JOptionPane.FRAMEBITS);
            }finally{
                try{
                    ps.close();
                    conn.close();
                }catch(Exception eee){
                    JOptionPane.showMessageDialog(null,"Connection Error !","warning",JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_delbtnActionPerformed

